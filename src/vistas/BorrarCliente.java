    private void jBBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBorrarActionPerformed
        // TODO add your handling code here:
        long dni = Long.parseLong(jTextDni.getText());
        TreeMap clienteBorrado = directorio.borrarCliente(dni);
        long clave1 =(long)clienteBorrado.firstKey();
        Cliente c = (Cliente) clienteBorrado.get(clave1);
        String clave =String.valueOf(clienteBorrado.firstKey());
        String nom = c.getNombre();
        String ape = c.getApellido();
        String ciu = c.getCiudad();
        
        
        jTextCiudad.setText(ciu);
        jTextApellido.setText(ape);
        jTextTelefono.setText(clave);
        jTextNombre.setText(nom);
        
    }//GEN-LAST:event_jBBorrarActionPerformed

